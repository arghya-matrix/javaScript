// loose Equality(==) Vs Strict Equality (===)

// loose equality operator
let age = 22;
console.log(age=="22");// type is ignored in loose equality, just the values are checked

// strict equality operator
console.log(age === "22"); // Focused on both the value and the type


// Not equal to operator

console.log(age!="22");// Loose not equality operator

console.log(age!=="22"); // strict not equality operator

